#include "../../includes/ft3d.h"

void	set_identity(t_m4 *mat)
{
	int	i;

	i = 0;
	while (i < 16)
	{
		(*mat)[i] = 0;
		if (i % 5 == 0)
			(*mat)[i] = 1;
		i++;
	}
}

void	set_null(t_m4 *mat)
{
	int	i;

	i = 0;
	while (i < 16)
	{
		(*mat)[i] = 0;
		i++;
	}
}
